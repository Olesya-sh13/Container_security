# ── Helper: set paragraph font & spacing ────────────────────────────────────
def fmt(para, size=14, bold=False, italic=False, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
        space_before=0, space_after=6, first_indent=None):
    para.alignment = align
    pf = para.paragraph_format
    pf.space_before = Pt(space_before)
    pf.space_after  = Pt(space_after)
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    if first_indent is not None:
        pf.first_line_indent = Cm(first_indent)
    for run in para.runs:
        run.font.name = "Times New Roman"
        run.font.size = Pt(size)
        run.font.bold   = bold
        run.font.italic = italic

def add_text(para_text, size=14, bold=False, italic=False,
             align=WD_ALIGN_PARAGRAPH.JUSTIFY,
             space_before=0, space_after=6, first_indent=1.25):
    p = doc.add_paragraph()
    p.add_run(para_text)
    fmt(p, size=size, bold=bold, italic=italic, align=align,
        space_before=space_before, space_after=space_after,
        first_indent=first_indent)
    return p

