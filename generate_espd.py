# ── helpers ──────────────────────────────────────────────────────────────────
def _run_fmt(run, size=14, bold=False, italic=False, font=TNR):
    run.font.name = font; run.font.size = Pt(size)
    run.font.bold = bold; run.font.italic = italic

def _para_fmt(p, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
              sb=0, sa=6, fi=None, ls=WD_LINE_SPACING.ONE_POINT_FIVE):
    p.alignment = align
    pf = p.paragraph_format
    pf.space_before = Pt(sb); pf.space_after = Pt(sa)
    pf.line_spacing_rule = ls
    if fi is not None: pf.first_line_indent = Cm(fi)

def h1(text):
    p = doc.add_paragraph(); r = p.add_run(text)
    _run_fmt(r, bold=True); _para_fmt(p, WD_ALIGN_PARAGRAPH.CENTER, sb=10, sa=6, fi=0)

def h2(text):
    p = doc.add_paragraph(); r = p.add_run(text)
    _run_fmt(r, bold=True); _para_fmt(p, WD_ALIGN_PARAGRAPH.LEFT, sb=6, sa=4, fi=0)

def h3(text):
    p = doc.add_paragraph(); r = p.add_run(text)
    _run_fmt(r, italic=True); _para_fmt(p, WD_ALIGN_PARAGRAPH.LEFT, sb=4, sa=4, fi=0)

def body(text, indent=1.25):
    p = doc.add_paragraph(); r = p.add_run(text); _run_fmt(r)
    _para_fmt(p, fi=indent)

def bul(text):
    p = doc.add_paragraph(); r = p.add_run("– " + text); _run_fmt(r)
    _para_fmt(p, fi=1.0)

def code(text):
    p = doc.add_paragraph(); r = p.add_run(text); _run_fmt(r, size=10, font=CNR)
    _para_fmt(p, WD_ALIGN_PARAGRAPH.LEFT, sb=0, sa=0, fi=0,
              ls=WD_LINE_SPACING.SINGLE)
    p.paragraph_format.left_indent = Cm(1.25)

def pb(): doc.add_page_break()

def tcap(text):
    p = doc.add_paragraph(); r = p.add_run(text); _run_fmt(r, size=12, italic=True)
    _para_fmt(p, WD_ALIGN_PARAGRAPH.LEFT, fi=0, sa=3)

def mk_table(headers, rows, sz=11):
    tbl = doc.add_table(rows=1+len(rows), cols=len(headers))
    tbl.style = 'Table Grid'
    for j, h in enumerate(headers):
        c = tbl.rows[0].cells[j]; c.text = h
        for para in c.paragraphs:
            for run in para.runs:
                run.font.name = TNR; run.font.size = Pt(sz); run.font.bold = True
    for i, row in enumerate(rows, 1):
        for j, v in enumerate(row):
            c = tbl.rows[i].cells[j]; c.text = str(v)
            for para in c.paragraphs:
                for run in para.runs:
                    run.font.name = TNR; run.font.size = Pt(sz)
    return tbl

